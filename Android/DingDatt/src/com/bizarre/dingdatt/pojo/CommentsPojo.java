package com.bizarre.dingdatt.pojo;

public class CommentsPojo {

	String comment_id = "";
	String contest_participant_id = "";
	String comment = "";
	String userid = "";
	String firstname = "";
	String profilepicture = "";

	public String getComment_id() {
		return comment_id;
	}

	public void setComment_id(String comment_id) {
		this.comment_id = comment_id;
	}

	public String getContest_participant_id() {
		return contest_participant_id;
	}

	public void setContest_participant_id(String contest_participant_id) {
		this.contest_participant_id = contest_participant_id;
	}

	public String getComment() {
		return comment;
	}

	public void setComment(String comment) {
		this.comment = comment;
	}

	public String getUserid() {
		return userid;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}

	public String getFirstname() {
		return firstname;
	}

	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}

	public String getProfilepicture() {
		return profilepicture;
	}

	public void setProfilepicture(String profilepicture) {
		this.profilepicture = profilepicture;
	}

}
