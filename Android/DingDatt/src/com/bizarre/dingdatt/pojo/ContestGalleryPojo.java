package com.bizarre.dingdatt.pojo;

public class ContestGalleryPojo {

	private String id = "";
	private String contest_id = "";
	private String user_id = "";
	private String uploadfile = "";
	private String uploaddate = "";
	private String uploadtopic = "";
	private String votingstatus = "";
	private int following = 0;
	private String name = "";
	private String profilepic = "";

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getContest_id() {
		return contest_id;
	}

	public void setContest_id(String contest_id) {
		this.contest_id = contest_id;
	}

	public String getUser_id() {
		return user_id;
	}

	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}

	public String getUploadfile() {
		return uploadfile;
	}

	public void setUploadfile(String uploadfile) {
		this.uploadfile = uploadfile;
	}

	public String getUploaddate() {
		return uploaddate;
	}

	public void setUploaddate(String uploaddate) {
		this.uploaddate = uploaddate;
	}

	public String getUploadtopic() {
		return uploadtopic;
	}

	public void setUploadtopic(String uploadtopic) {
		this.uploadtopic = uploadtopic;
	}

	public String getVotingstatus() {
		return votingstatus;
	}

	public void setVotingstatus(String votingstatus) {
		this.votingstatus = votingstatus;
	}

	public int getFollowing() {
		return following;
	}

	public void setFollowing(int following) {
		this.following = following;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getProfilepic() {
		return profilepic;
	}

	public void setProfilepic(String profilepic) {
		this.profilepic = profilepic;
	}
}
